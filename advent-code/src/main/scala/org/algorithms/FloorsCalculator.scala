package org.algorithms

import scala.annotation.tailrec

class FloorsCalculator(val src: String) {
    def evaluate: Int = {
        @tailrec
        def evaluate(src: String, acc: Int): Int = {
            if(src.isEmpty) acc
            else if(src.charAt(0) == '(') evaluate(src.substring(1), acc + 1)
            else evaluate(src.substring(1), acc - 1)
        }
        evaluate(src, 0)
    }
}

object FloorsCalculator {
    def apply(src: String): org.algorithms.FloorsCalculator = new FloorsCalculator(src)
}

